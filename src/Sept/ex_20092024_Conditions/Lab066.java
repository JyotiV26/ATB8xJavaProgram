package Sept.ex_20092024_Conditions;

public class Lab066 {
    //Example of Ternary operator
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int max = a > b ? a : b;
        int min = a < b ? a : b;
        System.out.println(max);
        System.out.println(min);
    }
}
