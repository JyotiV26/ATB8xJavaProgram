package Sept.ex_16092024;

public class Lab031 {
    public static void main(String[] args) {
        // Arithmetic Operators
        // +,-,/,*
        int a = 10;
        int b = 2;
        System.out.println(a + b); // + -> binary - 2 operands
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        // % - Modulus - Remainders, / - Quotient

        System.out.println(a % b);
        System.out.println(67 % 13);
    }
}
